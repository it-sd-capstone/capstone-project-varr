package edu.cvtc.varr;

public class Dungeon {
    private String name;
    private int level;

    public Dungeon() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }
}
