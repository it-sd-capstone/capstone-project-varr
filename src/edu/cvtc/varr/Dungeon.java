package edu.cvtc.varr;

public class Dungeon {
    private String name;
    private String level;

    public Dungeon() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }
}
